/**
 * Get all coordinates for a given control
 */
export function getControlCoordinates(control: Control): Coordinate[] {
  return controlCoordinates[control] ?? []
}

/**
 * Check if a coordinate is occupied by a given control
 */
export function isControlAtCoordinate(
  control: Control,
  x: number,
  y: number,
): boolean {
  const coords = getControlCoordinates(control)
  return coords.some((c) => c.x === x && c.y === y)
}
