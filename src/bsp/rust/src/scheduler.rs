#[unsafe(no_mangle)]
pub extern "C" fn addRepeatingTask(
    task: TaskHandle,
    priority: u8,
    backOffTime: f64,
    targetTimeBetweenCalls: f64,
    _maxTimeBetweenCalls: f64,
    _name: *const core::ffi::c_char,
    resource: ResourceID,
) -> TaskID {
    // Priority 0 is the audio routine: route it to the preemptive audio
    // interrupt-executor (see AUDIO_EXEC in main) so a thread-mode storage spin
    // can't starve it. All other priorities run cooperatively in thread mode.
    let on_audio = priority == 0;
    claim(
        task,
        None,
        secs_to_us(targetTimeBetweenCalls),
        secs_to_us(backOffTime),
        resource,
        false,
        true,
        on_audio,
    )
}

#[unsafe(no_mangle)]
pub extern "C" fn addOnceTask(
    task: TaskHandle,
    _priority: u8,
    timeToWait: f64,
    _name: *const core::ffi::c_char,
    resource: ResourceID,
) -> TaskID {
    claim(
        task,
        None,
        secs_to_us(timeToWait),
        0,
        resource,
        true,
        true,
        false,
    )
}

#[unsafe(no_mangle)]
pub extern "C" fn addConditionalTask(
    task: TaskHandle,
    _priority: u8,
    condition: RunCondition,
    _name: *const core::ffi::c_char,
    resource: ResourceID,
) -> TaskID {
    claim(task, condition, 0, 0, resource, true, true, false)
}

#[unsafe(no_mangle)]
pub extern "C" fn removeTask(id: TaskID) {
    if let Some(slot) = slot_of(id) {
        slot.stop.store(true, Ordering::Relaxed);
        slot.wake.signal(());
    }
}

#[unsafe(no_mangle)]
pub extern "C" fn blockTask(id: TaskID) {
    if let Some(slot) = slot_of(id) {
        slot.enabled.store(false, Ordering::Relaxed);
        slot.wake.signal(());
    }
}

#[unsafe(no_mangle)]
pub extern "C" fn unblockTask(id: TaskID) {
    if let Some(slot) = slot_of(id) {
        slot.enabled.store(true, Ordering::Relaxed);
        slot.wake.signal(());
    }
}

#[unsafe(no_mangle)]
pub extern "C" fn boostTask(id: TaskID) {
    if let Some(slot) = slot_of(id) {
        slot.boosted.store(true, Ordering::Relaxed);
        slot.wake.signal(());
    }
}

#[unsafe(no_mangle)]
pub extern "C" fn runTask(id: TaskID) {
    // Wake the task so it runs as soon as the executor next polls it.
    if let Some(slot) = slot_of(id) {
        slot.wake.signal(());
    }
}

#[unsafe(no_mangle)]
pub extern "C" fn setNextRunTimeforCurrentTask(seconds: f64) {
    if let Some(slot) = current_slot() {
        slot.next_override_us
            .store(secs_to_us(seconds), Ordering::Relaxed);
    }
}

#[unsafe(no_mangle)]
pub extern "C" fn ignoreForStats() {
    if let Some(slot) = current_slot() {
        slot.count_stats.store(false, Ordering::Relaxed);
    }
}

#[unsafe(no_mangle)]
pub extern "C" fn getAverageRunTimeForTask(id: TaskID) -> f64 {
    slot_of(id).map_or(0.0, |s| us_to_secs(s.avg_run_us.load(Ordering::Relaxed)))
}

#[unsafe(no_mangle)]
pub extern "C" fn getAverageRunTimeforCurrentTask() -> f64 {
    current_slot().map_or(0.0, |s| us_to_secs(s.avg_run_us.load(Ordering::Relaxed)))
}

#[unsafe(no_mangle)]
pub extern "C" fn getSystemTime() -> f64 {
    us_to_secs(Instant::now().as_micros())
}

#[unsafe(no_mangle)]
pub extern "C" fn isSDRoutineActive() -> bool {
    // The app reads this to avoid actions that can't run inside a slow-storage
    // busy-wait. On this BSP there is no such busy-wait: SD goes through the async
    // driver under `block_on` (which parks the executor, so nothing else runs during
    // a transfer anyway) and the legacy `yieldingRoutine*ForSD` hooks aren't linked.
    // So it's always false here.
    false
}

/// On the Embassy BSP the executor *is* the task manager loop, so there is no
/// blocking "start" — tasks are already spawned by `add*Task`. Present only to
/// satisfy the ABI; the bare-metal `deluge_main` path is the one that calls it.
#[unsafe(no_mangle)]
pub extern "C" fn startTaskManager() {}

// ---------------------------------------------------------------------------
// Cooperative yield (scheduler_api.h yield / yieldWithTimeout / yieldToIdle).
//
// The yielding operations (song load, stem export, grid clip create) run on the
// worker fiber (see fiber.rs), so `yield()` suspends the fiber and the executor
// runs everything else until the predicate holds. There is no off-fiber yield on
// this BSP — all yielding ops are dispatched to the worker via deluge_worker_run —
// so the off-fiber path is a defensive no-wait (it never busy-waits and never
// hangs). The legacy `storage_wait.h` hooks (yieldingRoutine*ForSD) are gone here:
// their only callers live in the legacy C HAL (src/RZA1 / src/bsp/rza1), which the
// Embassy BSP does not link — SD goes through the async driver (sd.rs).
// ---------------------------------------------------------------------------

/// Defensive fallback when a `yield*` is somehow reached off the worker fiber
/// (shouldn't happen — every yielding op is dispatched to the worker). Evaluates
/// the predicate once and returns it; never waits, never hangs.
fn off_fiber_yield(until: RunCondition) -> bool {
    // SAFETY: `until` is the C++ caller's predicate; calling it is benign polling.
    until.map_or(true, |p| unsafe { p() })
}

#[unsafe(export_name = "yield")]
pub extern "C" fn r#yield(until: RunCondition) {
    if crate::fiber::on_fiber() {
        crate::fiber::yield_until(until, None);
    } else {
        off_fiber_yield(until);
    }
}

#[unsafe(no_mangle)]
pub extern "C" fn yieldWithTimeout(until: RunCondition, timeout: f64) -> bool {
    if crate::fiber::on_fiber() {
        crate::fiber::yield_until(until, Some(secs_to_us(timeout)))
    } else {
        off_fiber_yield(until)
    }
}

#[unsafe(no_mangle)]
pub extern "C" fn yieldToIdle(until: RunCondition) -> bool {
    if crate::fiber::on_fiber() {
        crate::fiber::yield_until(until, None)
    } else {
        off_fiber_yield(until)
    }
}
